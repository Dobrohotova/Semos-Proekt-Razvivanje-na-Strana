					<p>Семос Академијата за дизајн распишува конкурс за запишување студенти на стручни студии во академската 2017/2018 година со почеток на наставата на 15.09.2017 година. Школувањето трае една академска година во два семестри (зимски и летен). Студентите ја посетуваат наставата секој работен ден од понеделник до петок.</p>

					<h2>I. БРОЈ НА СТУДЕНТИ</h2>
					<p>Во академската 2017/2018 година на Семос Академијата за дизајн има слободни места да се запишат вкупно 18 студенти.
					<br/>
					Доколку има слободни места после датумот предвиден за почеток на настава, може да се прифатат пријави на нови студенти.</p>

