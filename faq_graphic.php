							<li>Диплома за завршено средно образование - Диплома за положена државна матура;</li>
							<li>Диплома за завршени студии за лица кои завршиле високо или вишо образование;</li>
						</ul>
					
					<p>Документите се доставуваат секој ден од понеделник до петок од 10:00 до 18:00 часот, во просториите на Семос 	Компјутерски Образовен Центар во Скопје на Бул. К.Ј.Питу бр.19/6.</p>

