							<li>Лица кои се редовни студенти;</li>
							<li>Лица кои завршиле високо или вишо образование.</li>
						</ul>
					
					<p>Кандидатите при запишувањето склучуваат Договор за стручна обука. Кандидатите кои школарината ќе ја плаќаат на еднакви месечни рати, при запишувањето вршат уплата на депозит во денарска противвредност од 500 EUR.</p>

